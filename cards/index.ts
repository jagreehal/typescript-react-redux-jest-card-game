export function shuffle(input: Array<Card>): Array<Card> {
  let cards = input.slice(0);
  let currentIndex = cards.length, temporaryValue, randomIndex;

  // While there remain elements to shuffle...
  while (0 !== currentIndex) {
    // Pick a remaining element...
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex -= 1;

    // And swap it with the current element.
    temporaryValue = cards[currentIndex];
    cards[currentIndex] = cards[randomIndex];
    cards[randomIndex] = temporaryValue;
  }

  return cards;
}

export function notNextToEachOther(cards: Array<Card>): boolean {
  for (var i = 0; i < cards.length - 1; i++) {
    if (cards[i].value === cards[i + 1].value) {
      return false;
    }
  }
  return true;
}

export function getShuffledCardsForGame(input:Array<Card>): Array<Card> {
  let cards = shuffle(input.slice(0));
  while (!notNextToEachOther(cards)) {
    cards = shuffle(input.slice(0));
  }
  return cards;
}
